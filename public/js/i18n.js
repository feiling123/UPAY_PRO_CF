export const lang = chooseLanguage();
document.documentElement.lang = lang === "zh" ? "zh-CN" : "en";

export function t(key, fallback = "") {
  return dictionaries[lang]?.[key] || dictionaries.zh[key] || fallback || key;
}

export function applyStaticI18n(root = document) {
  root.querySelectorAll("[data-i18n]").forEach((node) => {
    node.textContent = t(node.dataset.i18n, node.textContent);
  });
  root.querySelectorAll("[data-i18n-placeholder]").forEach((node) => {
    node.setAttribute("placeholder", t(node.dataset.i18nPlaceholder, node.getAttribute("placeholder") || ""));
  });
  root.querySelectorAll("[data-i18n-title]").forEach((node) => {
    node.setAttribute("title", t(node.dataset.i18nTitle, node.getAttribute("title") || ""));
  });
  const title = root.querySelector("title[data-i18n]");
  if (title) document.title = t(title.dataset.i18n, title.textContent);
}

function chooseLanguage() {
  const languages = navigator.languages?.length ? navigator.languages : [navigator.language || "zh-CN"];
  return languages.some((item) => String(item).toLowerCase().startsWith("zh")) ? "zh" : "en";
}

applyStaticI18n();
